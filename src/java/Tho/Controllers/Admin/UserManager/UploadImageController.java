package Tho.Controllers.Admin.UserManager;

import Tho.Models.UserDAO;
import java.io.File;
import java.io.IOException;
import java.util.Hashtable;
import java.util.Iterator;
import java.util.List;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.apache.tomcat.util.http.fileupload.FileItem;
import org.apache.tomcat.util.http.fileupload.FileItemFactory;
import org.apache.tomcat.util.http.fileupload.disk.DiskFileItemFactory;
import org.apache.tomcat.util.http.fileupload.servlet.ServletFileUpload;
import org.apache.tomcat.util.http.fileupload.servlet.ServletRequestContext;

/**
 *
 * @author ThoDT
 */
public class UploadImageController extends HttpServlet {

    private static final String ERROR = "error.jsp", 
//            SEARCH = "UserManager.SearchController",
            VIEW_INFO = "UserManager.ViewInfoController";

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String url = ERROR;
        try {
            boolean isMultiPart = ServletFileUpload.isMultipartContent(request);
            if (isMultiPart) {
                FileItemFactory factory = new DiskFileItemFactory();
                ServletFileUpload upload = new ServletFileUpload(factory);
                List items = upload.parseRequest(new ServletRequestContext(request));

                Iterator iter = items.iterator();
                Hashtable params = new Hashtable();
                String fileName = null;
                FileItem savedItem = null;
                while (iter.hasNext()) {
                    FileItem item = (FileItem) iter.next();
                    if (item.isFormField()) {
                        params.put(item.getFieldName(), item.getString());
                    } else {
                        savedItem = item;
                    }
                }

                String username = (String) params.get("txtUsername");
                String search = (String) params.get("txtSearch");
                if (savedItem != null) {
                    String itemName = savedItem.getName();
                    fileName = itemName.substring(itemName.indexOf("\\") + 1);
                    String extension = fileName.substring(fileName.indexOf(".") + 1);
                    String realPath = getServletContext().getRealPath("/") + "src\\img\\" + username + "." + extension;
                    File savedFile = new File(realPath);
                    savedItem.write(savedFile);
                    if (new UserDAO().updateAvatarUser(username, "src/img/" + username + "." + extension)) {
                        url = VIEW_INFO + "?txtUsername=" + username + "&txtSearch=" + search;
                    } else {
                        request.setAttribute("ERROR", "Upload avatar failed");
                    }
                }
            }
        } catch (Exception e) {
            log("Error at UploadImageController", e);
        } finally {
            request.getRequestDispatcher(url).forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
